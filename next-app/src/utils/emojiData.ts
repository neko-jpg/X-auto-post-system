/**
 * Get all emojis in a flat array
 */
export function getAllEmojis(): string[] {
    const seen = new Set<string>();
    const result: string[] = [];

    for (const category of emojiCategories) {
        for (const emoji of category.emojis) {
            if (!seen.has(emoji)) {
                seen.add(emoji);
                result.push(emoji);
            }
        }
    }

    return result;
}

/**
 * Search emojis by category name
 */
export function searchEmojis(query: string): string[] {
    const lowerQuery = query.toLowerCase();
    const results: string[] = [];

    for (const category of emojiCategories) {
        if (
            category.name.toLowerCase().includes(lowerQuery) ||
            category.nameJp.includes(query) ||
            category.id.includes(lowerQuery)
        ) {
            results.push(...category.emojis);
        }
    }

    return [...new Set(results)];
}

/**
 * Get category by ID
 */
export function getCategoryById(id: string): EmojiCategory | undefined {
    return emojiCategories.find(c => c.id === id);
}
